public class Calculator {

    int number1;
    int number2;

    double number3;

    double number4;

    //Add function
    public int addFunction(int number1, int number2) {
        return number1 + number2;
    }

    //Subtract function
    public int subtractFunction(int number1, int number2) {
        return number1 - number2;
    }

    //Multiply function
    public int multiplyFunction(int number1, int number2) {
        return number1 * number2;
    }

    //Divide function
    public double divideFunction(double number3, double number4) {
        return number3 / number4;
    }






}
